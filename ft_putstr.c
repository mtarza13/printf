#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	x;

	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	x = ft_strlen(str);
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
	return (x);
}
